function calcSlice(arr: number[], start: number, end: number, isReverse = false) {
  let res: number[] = [];
  if (isReverse) {
    for (let i = end * 2 - 2; i >= start * 2; i -= 2) {
      res.push(arr[i], arr[i + 1]);
    }
  } else {
    res = arr.slice(start * 2, end * 2)
  }
  return res;
}


